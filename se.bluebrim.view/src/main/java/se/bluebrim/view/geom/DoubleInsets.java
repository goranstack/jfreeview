package se.bluebrim.view.geom;

/**
 * @author G Stack
 */
public class DoubleInsets
{
	public double top;
	public double left;
	public double bottom;
	public double right;
	
	public DoubleInsets(double top, double left, double bottom, double right)
	{
		this.top = top;
		this.left = left;
		this.bottom = bottom;
		this.right = right;
	}

}
