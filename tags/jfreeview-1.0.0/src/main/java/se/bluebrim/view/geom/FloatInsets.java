package se.bluebrim.view.geom;


public class FloatInsets
{
	public float top;
	public float left; 
	public float bottom;
	public float right;
	
	public FloatInsets()
	{
	}
	
	public FloatInsets(float top, float left, float bottom, float right)
	{
		this.top = top;
		this.left = left; 
		this.bottom = bottom;
		this.right = right;
	}


}